     <h1> О проекте </h1>
      <p> Remindio &ndash; это сервис для напоминаний о днях рождения наших друзей, родных, коллег. </p> 
      <p>
        Каждый из нас всегда чем-то занят: учёба, работа, отдых, развлечения. В этих делах мы часто забываем поздравить близких нам людей с днём рождения. 
        Некоторых друзей поздравить помогают соцсети, однако не у всех в профилях указана дата рождения, некоторые друзья не сидят в них или сидят, но в других. 
        Некоторые люди слишком скромны, чтобы засорять Интернет многочисленными фото со своих праздников. Ну а некоторые застали ещё времена, когда позравления слали по телеграфу.
      </p>
      <p>
        Мы подумали, что будет здорово собрать в одном месте, как в записной книжке, всех, кого мы в течении года хотели бы поздравить с днем рождения.
      </p>
      <p>
        А главное &ndash; настроить уведомления, которые бы нам об этом напоминали!
      </p>

      <h2> Как это работает? </h2>
      
      <p>
        Суть работы сервиса очень проста. Нужно ввести имя именника и выбрать, за сколько дней до дня рождения должно прийти уведомление &ndash; напоминалка. Всего можно выбрать до трёх уведомлений, например, за 14, 7 и 5 дней до праздника.
      </p>
      
      <p>
        Имена именинников хранятся в зашифрованном виде.
